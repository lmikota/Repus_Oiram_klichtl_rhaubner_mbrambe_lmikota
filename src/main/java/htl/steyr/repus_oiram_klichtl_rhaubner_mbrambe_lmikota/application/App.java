    private void updateGame(int map[][], ImageView player, Pane root, int screenWidth) {
        playerMovementX(player);
        playerMovementY(player,map);
        moveRoot(root, player, screenWidth);
    }

    public void playerMovementY(ImageView player, int map[][]) {
        if (pressedKeys.contains(KeyCode.SPACE) && !isJumping) {
            playerVelY = JUMP_SPEED;
            isJumping = true;
        }

        playerVelY += GRAVITY;
        double newYPosition = player.getY() + playerVelY;

        int blockX = (int)  (player.getX() / 40);
        int blockY = (int) ((newYPosition + player.getFitHeight()) / 40);

        if (blockY < map.length && map[blockY][blockX] == 1) {
            player.setY(blockY * 40 - player.getFitHeight());
            playerVelY = 0;
            isJumping = false;
        } else {
            player.setY(newYPosition);
        }
    }

    public void playerMovementX(ImageView player) {
        if(pressedKeys.contains(KeyCode.D)) {
            playerX += MOVE_SPEED;
        }else if(pressedKeys.contains(KeyCode.A)) {
            playerX -= MOVE_SPEED;
        }
        player.setX(playerX);
    }

    public void moveRoot(Pane root, ImageView player,int screenWidth) {
        double playerScreenX = player.getX() - offsetX;

        if (playerScreenX > screenWidth * 0.75) {
            offsetX += SCROLL_SPEED;
        } else if (playerScreenX < screenWidth * 0.25) {
            offsetX -= SCROLL_SPEED;
        }
        offsetX = Math.max(0, Math.min(offsetX, 1400 * 40 - screenWidth));
        root.setTranslateX(-offsetX);
    }

    public void addToRoot(Pane root, Node node) {
        root.getChildren().add(node);
    }

